class ProductModel {
    static getAll() {
        return products;
    }

    static getById(id) {
        return products.find((p) => p.id === id);
    }

    static create(productData) {
        const newProduct = {
            id: `p${Date.now()}`,
            createdAt: new Date(),
            ...productData,
        };
        products.push(newProduct);
        return newProduct;
    }

    static exists(id) {
        return products.some((p) => p.id === id);
    }
}

module.exports = ProductModel;